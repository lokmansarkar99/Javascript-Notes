// The window Object
// The window object is the global object in the browser. It represents the browser window or tab and provides access to various browser-related functionalities, including handling global variables and functions.

// Example: Using window object

// Accessing window properties
console.log(window.innerWidth); // The inner width of the browser window
console.log(window.innerHeight); // The inner height of the browser window
console.log(window.location.href); // Current URL of the page



